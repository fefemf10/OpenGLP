	inline string tolower(string_view s)
	{
		string tmp;
		transform(s.begin(), s.end(), tmp.begin(), [](unsigned char c) -> unsigned char { return tolower(c); });
		return tmp;
	}
	inline void tolower(string& s)
	{
		transform(s.begin(), s.end(), s.begin(), [](unsigned char c) -> unsigned char { return tolower(c); });
	}
